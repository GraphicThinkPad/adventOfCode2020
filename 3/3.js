const lengthenedInput = input.map((line) => line.repeat(1000));

function isTree(character) {
  return character === "#";
}

function rightXDownY(x, y, tobogganMap) {
  let treeCount = 0;
  let currentPosition = 0;
  for (let i = 0; i <= tobogganMap.length - 1; i += y) {
    const currentLine = tobogganMap[i];
    if (isTree(currentLine[currentPosition])) {
      treeCount++;
    }
    currentPosition += x;
  }
  return treeCount;
}

const right1Down1 = rightXDownY(1, 1, lengthenedInput);
